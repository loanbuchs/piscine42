#include <unistd.h>
#include "ft_stock_str.h"

void	ft_putstr(char *str)
{
	int	caractere;
	int	i;

	i = 0;
	while (str[i])
	{
		caractere = str[i];
		write(1, &caractere, 1);
		i++;
	}
}

void	ft_putnbr(int nbr)
{
	if (nbr > 9)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else
		write(1, &(char){nbr + '0'}, 1);
}

void	ft_show_tab(struct s_stock_str *par)
{
	int	i;

	i = 0;
	while (par[i].str)
	{
		ft_putstr(par[i].str);
		write(1, "\n", 1);
		ft_putnbr(par[i].size);
		write(1, "\n", 1);
		ft_putstr(par[i].copy);
		write(1, "\n", 1);
		i++;
	}
}
