char	*ft_strlowcase(char	*str)
{
	int	i;

	i = 0;
	while (str[i] != 0)
	{
		if ((64 < str[i]) && (str[i] < 91))
		{
			str[i] = str[i] + 32;
			i++;
		}
		else
			i++;
	}
	return (str);
}
